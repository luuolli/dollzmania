import 'package:flutter/material.dart';

abstract class AppColors {
  AppColors._();

  static Color get white => const Color(0xFFFFFFFF);
  static Color get darkBlue => const Color(0xFF0D5878);
  static Color get lightPink => const Color(0xFFFAF2FF);
  static Color get purple => const Color(0xFF8552D9);
  static Color get black => const Color(0xFF141414);
  static Color get black10 => const Color(0xFF505050);
  static Color get grey => const Color(0xFFC4C4C4);
  static Color get green => const Color(0xFF5CBF2D);
  static Color get transparent => const Color(0x00000000);
}
